#include "Calculator.h"

int Calculator::minus(int number1, int number2)
{
	return number1 - number2;
}

int Calculator::add(int num1, int num2)
{
	return num1 + num2;
}

double Calculator::mult(double num, double factor)
{
	return num * factor;
}
